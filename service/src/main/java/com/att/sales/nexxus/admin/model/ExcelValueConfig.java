package com.att.sales.nexxus.admin.model;

/**
 * The Class ExcelValueConfig.
 *
 * @author vt393d
 */
public class ExcelValueConfig {

	/** The excel header. */
	private String excelHeader;
	
	/** The excel index. */
	private int excelIndex;
	
	/** The excel cell address. */
	private String excelCellAddress;
	
	/** The column type. */
	private String columnType;
	
	/** The excel value. */
	private String excelValue;
	
	/** The table column name. */
	private String tableColumnName;
	
	/**
	 * Gets the excel header.
	 *
	 * @return the excel header
	 */
	public String getExcelHeader() {
		return excelHeader;
	}
	
	/**
	 * Sets the excel header.
	 *
	 * @param excelHeader the new excel header
	 */
	public void setExcelHeader(String excelHeader) {
		this.excelHeader = excelHeader;
	}
	
	/**
	 * Gets the excel index.
	 *
	 * @return the excel index
	 */
	public int getExcelIndex() {
		return excelIndex;
	}
	
	/**
	 * Sets the excel index.
	 *
	 * @param excelIndex the new excel index
	 */
	public void setExcelIndex(int excelIndex) {
		this.excelIndex = excelIndex;
	}
	
	/**
	 * Gets the excel cell address.
	 *
	 * @return the excel cell address
	 */
	public String getExcelCellAddress() {
		return excelCellAddress;
	}
	
	/**
	 * Sets the excel cell address.
	 *
	 * @param excelCellAddress the new excel cell address
	 */
	public void setExcelCellAddress(String excelCellAddress) {
		this.excelCellAddress = excelCellAddress;
	}
	
	/**
	 * Gets the column type.
	 *
	 * @return the column type
	 */
	public String getColumnType() {
		return columnType;
	}
	
	/**
	 * Sets the column type.
	 *
	 * @param columnType the new column type
	 */
	public void setColumnType(String columnType) {
		this.columnType = columnType;
	}
	
	/**
	 * Gets the excel value.
	 *
	 * @return the excel value
	 */
	public String getExcelValue() {
		return excelValue;
	}
	
	/**
	 * Sets the excel value.
	 *
	 * @param excelValue the new excel value
	 */
	public void setExcelValue(String excelValue) {
		this.excelValue = excelValue;
	}
	
	/**
	 * Gets the table column name.
	 *
	 * @return the table column name
	 */
	public String getTableColumnName() {
		return tableColumnName;
	}
	
	/**
	 * Sets the table column name.
	 *
	 * @param tableColumnName the new table column name
	 */
	public void setTableColumnName(String tableColumnName) {
		this.tableColumnName = tableColumnName;
	}
	
	
}
